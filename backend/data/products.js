const slugify = (str) => {
  return str
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "") // bỏ dấu tiếng Việt
    .toLowerCase()
    .trim()
    .replace(/\s+/g, "-") // khoảng trắng -> dấu -
    .replace(/[^a-z0-9-]/g, ""); // bỏ ký tự đặc biệt
};

